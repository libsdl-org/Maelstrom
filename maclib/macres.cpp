static void ExportFont(Mac_Resource *res, const char *type, Uint16 id, const char *basepath)
{
	char path[PATH_MAX];
	SDL_snprintf(path, sizeof(path), "%s/Fonts", basepath);
	SDL_CreateDirectory(path);
	SDL_snprintf(path, sizeof(path), "%s/Fonts/%s#%d", basepath, type, id);

	FILE *output;
	Mac_ResData *entry;
	if ( (output=fopen(path, "w")) != NULL ) {
		entry = res->Resource(type, id);
		fwrite(entry->data, entry->length, 1,  output);
		fclose(output);			   
	}
}

static void ExportSound(Mac_Resource *res, const char *type, Uint16 id, const char *basepath)
{
	char path[PATH_MAX];
	SDL_snprintf(path, sizeof(path), "%s/Sounds", basepath);
	SDL_CreateDirectory(path);
	SDL_snprintf(path, sizeof(path), "%s/Sounds/snd#%d.wav", basepath, id);

	Mac_ResData *entry = res->Resource(type, id);
	Wave wave;
	wave.Load(entry, 0);
	wave.Save(path);
}

static void ExportSprite(Mac_Resource *res, const char *type, Uint16 id, const char *basepath)
{
	char path[PATH_MAX];
	SDL_snprintf(path, sizeof(path), "%s/Sprites", basepath);
	SDL_CreateDirectory(path);
	SDL_snprintf(path, sizeof(path), "%s/Sprites/%s#%d.png", basepath, type, id);

	bool large = (SDL_strcmp(type, "icl8") == 0);
	Mac_ResData *sprite_entry = res->Resource(type, id);
	Mac_ResData *mask_entry = res->Resource(large ? "ICN#" : "ics#", id);
	Uint8 *mask = mask_entry ? (mask_entry->data + (large ? 128 : 32)) : NULL;
	int size = (large ? 32 : 16);

	SDL_Surface *surface = SDL_CreateSurface(size, size, SDL_PIXELFORMAT_INDEX8);
	SDL_Palette *palette = SDL_CreateSurfacePalette(surface);
	SDL_SetPaletteColors(palette, colors, 0, 256);

	int i, j, used[256];
	Uint8 colorkey, m;
	Uint8 *src, *dst;

	/* Look for an unused palette entry */
	SDL_zeroa(used);
	src = sprite_entry->data;
	for ( i = 0; i < (size * size); ++i ) {
		++used[*src];
		++src;
	}
	for ( i = 0; used[i] && i < 256; ++i ) {
		/* Keep looking */;
	}
	colorkey = (Uint8)i;

	SDL_Color transparent = { 0, 0, 0, SDL_ALPHA_TRANSPARENT };
	SDL_SetPaletteColors(palette, &transparent, colorkey, 1);

	/* Copy over the pixels */
	src = sprite_entry->data;
	if ( mask ) {
		for ( i = 0; i < size; ++i ) {
			dst = (Uint8 *)surface->pixels + i*surface->pitch;
			for ( j = 0; j < size; ++j ) {
				if ( (j%8) == 0 ) {
					m = *mask++;
				}
				if ( m & 0x80 ) {
					*dst = *src;
				} else {
					*dst = colorkey;
				}
				m <<= 1;
				dst += 1;
				src += 1;
			}
		}
	} else {
		for ( i = 0; i < size; ++i ) {
			dst = (Uint8 *)surface->pixels + i*surface->pitch;
			for ( j = 0; j < size; ++j ) {
				*dst++ = *src++;
			}
		}
	}

	SDL_SavePNG(surface, path);
}

static void Export(Mac_Resource *res, const char *type, Uint16 id, const char *basepath)
{
	if ( SDL_strcmp(type, "FOND") == 0 || SDL_strcmp(type, "NFNT") == 0 ) {
		ExportFont(res, type, id, basepath);
	} else if ( SDL_strcmp(type, "snd ") == 0 ) {
		ExportSound(res, type, id, basepath);
	} else if ( SDL_strcmp(type, "ics8") == 0 || SDL_strcmp(type, "icl8") == 0 ) {
		ExportSprite(res, type, id, basepath);
	}
}

int main(int argc, char *argv[])
{
	Mac_Resource *res;
	char      **types;
	Uint16     *ids;
	int         arg, i, j;
	bool        export_res = false;

	if ( ! argv[1] ) {
		fprintf(stderr, "Usage: %s [--export] <Mac Resource File> [output_path]\n", argv[0]);
		exit(1);
	}
	arg = 1;

	if ( SDL_strcmp(argv[arg], "--export") == 0 ) {
		export_res = true;
		++arg;
	}

	res = new Mac_Resource(argv[arg]);
	if ( res->Error() ) {
		fprintf(stderr, "Mac_Resource: %s\n", res->Error());
		delete res;
		exit(2);
	}
	++arg;
	
	types = res->Types();
	for ( i=0; types[i]; ++i ) {
		ids = res->ResourceIDs(types[i]);
		printf("Resource set: type = '%s', contains %hd resources\n",
					types[i], res->NumResources(types[i]));
		for ( j=0; ids[j] < 0xFFFF; ++j ) {
			printf("\tResource %d (ID = %d): \"%s\"\n", j+1,
				ids[j], res->ResourceName(types[i], ids[j]));
			if ( argv[arg] ) {
				if ( export_res ) {
					Export(res, types[i], ids[j], argv[arg]);
				} else {
					char path[23];
					SDL_snprintf(path, sizeof(path), "%s/%s:%hu", argv[arg], types[i], ids[j]);
					FILE *output;
					Mac_ResData *D;
					if ( (output=fopen(path, "w")) != NULL ) {
						D = res->Resource(types[i], ids[j]);
						fwrite(D->data, D->length, 1,  output);
						fclose(output);			   
					}
				}
			}
		}
		delete[]  ids;
	}
	delete[] types;
	delete res;
	exit(0);
}
